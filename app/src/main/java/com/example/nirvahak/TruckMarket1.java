package com.example.nirvahak;

public class TruckMarket1 {
    String Truck1;
    String TruckCategory1;
    String TruckRegisterNo1;
    String TruckTonnage1;
    String TruckOwnerNo1;


    public TruckMarket1() {
    }

    public TruckMarket1(String truck1, String truckCategory1, String truckRegisterNo1, String truckTonnage1, String truckOwnerNo1) {
        Truck1 = truck1;
        TruckCategory1 = truckCategory1;
        TruckRegisterNo1 = truckRegisterNo1;
        TruckTonnage1 = truckTonnage1;
        TruckOwnerNo1 = truckOwnerNo1;
    }

    public String getTruck1() {
        return Truck1;
    }

    public void setTruck1(String truck1) {
        Truck1 = truck1;
    }

    public String getTruckCategory1() {
        return TruckCategory1;
    }

    public void setTruckCategory1(String truckCategory1) {
        TruckCategory1 = truckCategory1;
    }

    public String getTruckRegisterNo1() {
        return TruckRegisterNo1;
    }

    public void setTruckRegisterNo1(String truckRegisterNo1) {
        TruckRegisterNo1 = truckRegisterNo1;
    }

    public String getTruckTonnage1() {
        return TruckTonnage1;
    }

    public void setTruckTonnage1(String truckTonnage1) {
        TruckTonnage1 = truckTonnage1;
    }

    public String getTruckOwnerNo1() {
        return TruckOwnerNo1;
    }

    public void setTruckOwnerNo1(String truckOwnerNo1) {
        TruckOwnerNo1 = truckOwnerNo1;
    }

}